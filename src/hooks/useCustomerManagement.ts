
import { useCallback } from 'react';
import { supabase } from '@/integrations/supabase/client';
import { CustomerFormData } from '@/types/checkout';

export const useCustomerManagement = () => {
  
  const createOrFindCustomer = useCallback(async (formData: CustomerFormData) => {
    console.log('üë§ Processando dados do cliente...');

    try {
      // Validar dados obrigat√≥rios
      if (!formData.nome?.trim() || !formData.email?.trim()) {
        throw new Error('Nome e email s√£o obrigat√≥rios');
      }

      // Validar formato do email
      const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
      if (!emailRegex.test(formData.email)) {
        throw new Error('Formato de email inv√°lido');
      }

      // Primeiro, tentar buscar cliente existente pelo email
      console.log('üîç Buscando cliente existente por email:', formData.email);
      
      const { data: existingCustomer, error: findError } = await supabase
        .from('clientes')
        .select('*')
        .eq('email', formData.email.toLowerCase().trim())
        .maybeSingle(); // Use maybeSingle() para evitar erro quando n√£o encontrar

      if (findError) {
        console.error('‚ùå Erro ao buscar cliente:', findError);
        // N√£o falhar por causa de erro de busca, continuar com cria√ß√£o
      }

      if (existingCustomer) {
        console.log('‚úÖ Cliente j√° existe:', existingCustomer.id);
        
        // Verificar se precisa atualizar algum dado (apenas nome e CPF, pois telefone n√£o existe na tabela)
        const needsUpdate = (
          existingCustomer.nome !== formData.nome ||
          existingCustomer.cpf !== (formData.cpf || null)
        );

        if (needsUpdate) {
          console.log('üîÑ Atualizando dados do cliente...');
          
          const { data: updatedCustomer, error: updateError } = await supabase
            .from('clientes')
            .update({
              nome: formData.nome,
              cpf: formData.cpf || null
            })
            .eq('id', existingCustomer.id)
            .select()
            .single();

          if (updateError) {
            console.warn('‚ö†Ô∏è Falha ao atualizar cliente, usando dados antigos:', updateError);
            return existingCustomer;
          }

          return updatedCustomer;
        }

        return existingCustomer;
      }

      // Se n√£o existe, criar novo cliente
      console.log('üìù Criando novo cliente...');
      const customerData = {
        nome: formData.nome.trim(),
        email: formData.email.toLowerCase().trim(),
        cpf: formData.cpf?.trim() || null
        // Note: telefone n√£o est√° inclu√≠do pois n√£o existe na tabela
      };

      const { data: newCustomer, error: createError } = await supabase
        .from('clientes')
        .insert(customerData)
        .select()
        .single();

      if (createError) {
        console.error('‚ùå Erro ao criar cliente:', createError);
        
        // Verificar se √© erro de duplicata (por race condition)
        if (createError.code === '23505') {
          console.log('üîÑ Cliente criado por outro processo, buscando novamente...');
          
          const { data: raceCustomer } = await supabase
            .from('clientes')
            .select('*')
            .eq('email', formData.email.toLowerCase().trim())
            .single();
            
          if (raceCustomer) {
            return raceCustomer;
          }
        }
        
        throw new Error('Falha ao registrar cliente: ' + createError.message);
      }

      console.log('‚úÖ Cliente criado com ID:', newCustomer.id);
      return newCustomer;

    } catch (error) {
      console.error('‚ùå Erro no processo de cliente:', error);
      throw error;
    }
  }, []);

  const getCustomerById = useCallback(async (customerId: string) => {
    try {
      const { data: customer, error } = await supabase
        .from('clientes')
        .select('*')
        .eq('id', customerId)
        .single();

      if (error) {
        console.error('‚ùå Erro ao buscar cliente por ID:', error);
        return null;
      }

      return customer;
    } catch (error) {
      console.error('‚ùå Erro ao buscar cliente:', error);
      return null;
    }
  }, []);

  return {
    createOrFindCustomer,
    getCustomerById
  };
};
